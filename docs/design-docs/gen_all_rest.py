def pg_doc(pg_id, pg_name, subsystem, overview, io_def, logic, valid_rules, transitions, struts_map=None):
    d = []
    d.append(hdr_row(['プログラム設計書', '', '', '', '', '', '', '']))
    d.append(hdr_row(['文書管理情報', '', '', '', '', '', '', '']))
    for r in [['システムID', 'STRUTSLAB-001', '', '', 'サブシステム名', subsystem, '', ''],
              ['プログラムID', pg_id, '', '', 'プログラム名', pg_name, '', ''],
              ['文書番号', f'DOC-PG-{pg_id}', '', '', '版数', '1.0', '', ''],
              ['作成日', '2026/05/23', '', '', '作成者', 'システム開発部', '', ''],
              ['区分', '新規', '', '', '', '', '', '']]:
        d.append(r)
    d.append(empty_row())
    d.append(hdr_row(['プログラム概要', '', '', '', '', '', '', '']))
    d.append([overview, '', '', '', '', '', '', ''])
    d.append(empty_row())
    d.append(hdr_row(['入出力定義', '', '', '', '', '', '', '']))
    d.append(hdr_row(['区分', '項目名', '型', 'Formプロパティ', '必須', '備考', '', '']))
    for r in io_def: d.append(list(r) + [''] * (8 - len(r)))
    d.append(empty_row())
    d.append(hdr_row(['処理詳細（Action.executeフロー）', '', '', '', '', '', '', '']))
    d.append(hdr_row(['No.', '処理内容', '関連DAO', '備考', '', '', '', '']))
    for i, r in enumerate(logic, 1):
        d.append([str(i), r[0], r[1] if len(r) > 1 else '', r[2] if len(r) > 2 else '', '', '', '', ''])
    d.append(empty_row())
    d.append(hdr_row(['入力検証ルール', '', '', '', '', '', '', '']))
    d.append(hdr_row(['No.', '対象項目', '検証ルール', 'エラーメッセージ（ja）', '', '', '', '']))
    for i, r in enumerate(valid_rules, 1):
        d.append([str(i)] + list(r) + [''] * (8 - 1 - len(r)))
    if transitions:
        d.append(empty_row())
        d.append(hdr_row(['状態遷移', '', '', '', '', '', '', '']))
        d.append(hdr_row(['遷移元', '', '遷移先', 'トリガー', '', '', '', '']))
        for t in transitions: d.append(list(t) + [''] * (8 - len(t)))
    if struts_map:
        d.append(empty_row())
        d.append(hdr_row(['struts-config.xml マッピング', '', '', '', '', '', '', '']))
        d.append(hdr_row(['項目', '値', '', '', '', '', '', '']))
        for k, v in struts_map.items(): d.append([k, v, '', '', '', '', '', ''])
    return d

